//Dias semana
var dia = parseInt(prompt("escolha o dia da semana"));
switch(dia){
	case 1:
		alert("hoje é Domingo");
	break;
	case 2:
		alert("hoje é Segunda");
	break;
	case 3:
		alert("hoje é terça");
	break;
	case 4:
		alert("hoje é quarta");
	break;
	case 5:
		alert("hoje é quinta");
	break;
	case 6:
		alert("hoje é sexta");
	break;
	case 7:
		alert("hoje é sabado");
	break;
	default:
	alert("erro")
	break;
}
